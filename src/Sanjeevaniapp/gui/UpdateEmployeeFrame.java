        public void loadEmployeesDetails(){
            try{
                employee=EmpDAO.getAllEmpData();
                if(employee.size()==0){
                JOptionPane.showMessageDialog(null,"No Employees Present!");
                btnUpdate.setEnabled(false);
                return;
                }
               Collection Keys=employee.keySet();
               Iterator<String> it=Keys.iterator();
               while(it.hasNext()){
                   jcbEmpId.addItem(it.next());
               }
            }
              catch(SQLException sqle){
          JOptionPane.showMessageDialog(null,"DBError:"+sqle,"Exception",JOptionPane.ERROR_MESSAGE);
          sqle.printStackTrace();
        }
        }
        
        
