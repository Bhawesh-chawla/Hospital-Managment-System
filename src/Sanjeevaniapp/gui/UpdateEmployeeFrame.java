    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
      if(validateInput()==false){
          JOptionPane.showMessageDialog(null,"Please input all the Values");
            return;}
      try{
         double esal=Double.parseDouble(txtSal.getText()); 
         e.setEmpid(jcbEmpId.getSelectedItem().toString());
         e.setJob(jcbjob.getSelectedItem().toString());
         e.setSal(esal);
         e.setEmpname(txtEmpName.getText());
          boolean Result=EmpDAO.updateEmp(e);
          if(Result)
          {JOptionPane.showMessageDialog(null,"Record Updated!");
            reset();}
          else 
              JOptionPane.showMessageDialog(null,"Record Not Updated !");
     } 
     catch(NumberFormatException nfe){
          JOptionPane.showMessageDialog(null,"Please inpuy digits only!","Error",JOptionPane.ERROR_MESSAGE );
          nfe.printStackTrace();
      }
      catch(SQLException sqle){
       JOptionPane.showMessageDialog(null,"Problem in DB!","Error",JOptionPane.ERROR_MESSAGE);
       sqle.printStackTrace();
      }
      
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void jcbEmpIdItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcbEmpIdItemStateChanged
    if(jcbEmpId.getItemCount()==0)
            return;
    else if(jcbEmpId.getSelectedIndex()!=0){
    e=employee.get(jcbEmpId.getSelectedItem().toString());
    txtEmpName.setText(e.getEmpname());
    txtSal.setText(Double.toString(e.getSal()));
    jcbjob.setSelectedItem(e.getJob());}
    
    }//GEN-LAST:event_jcbEmpIdItemStateChanged

