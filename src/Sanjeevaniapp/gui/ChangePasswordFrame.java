    private void btnUpdatePwdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdatePwdActionPerformed
     if(!validateInput()){
         return;
     }
     try{
         boolean result=UserDao.changePassword(jcbReceptionistId.getSelectedItem().toString(),password);
         if(!result){
             JOptionPane.showMessageDialog(null,"Record Unchanged","ERROR",JOptionPane.ERROR_MESSAGE);
         }
         else
         { JOptionPane.showMessageDialog(null,"Password Changed ","Success",JOptionPane.INFORMATION_MESSAGE);
             Reset();}
     }
      catch(SQLException sqle){
          JOptionPane.showMessageDialog(null,"DBError:"+sqle,"Exception",JOptionPane.ERROR_MESSAGE);
          sqle.printStackTrace();
        } 
    }//GEN-LAST:event_btnUpdatePwdActionPerformed

