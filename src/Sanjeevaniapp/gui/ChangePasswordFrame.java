    public void loadReceptionistList(){
        try{
            receptionistList=UserDao.getReceptionist();
            if(receptionistList.size()==0){
                JOptionPane.showMessageDialog(null,"No Receptionist Present!");
                btnUpdatePwd.setEnabled(false);
                return; 
            }
            Collection keys=receptionistList.keySet();
            Iterator<String> it=keys.iterator();
            while(it.hasNext()){
                jcbReceptionistId.addItem(it.next());
            }
        }
        catch(SQLException sqle){
          JOptionPane.showMessageDialog(null,"DBError:"+sqle,"Exception",JOptionPane.ERROR_MESSAGE);
          sqle.printStackTrace();
        }
    }
    
