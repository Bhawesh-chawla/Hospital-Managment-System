    public boolean validateInput(){
        if(txtpwd.getPassword().length==0||txtRePwd.getPassword().length==0){
            JOptionPane.showMessageDialog(null,"Please Enter both Password fields first","Error",JOptionPane.ERROR_MESSAGE);
             return false;}
        password=String.valueOf(txtpwd.getPassword());
        if(!password.equals(String.valueOf(txtRePwd.getPassword()))){
            JOptionPane.showMessageDialog(null,"Password fields not Matched","Error",JOptionPane.ERROR_MESSAGE);
            return false;}
            return true;
    }
