    public boolean validateInput(){
        char[] pwd=jpPass.getPassword();
        char[] repwd=jpRePass.getPassword();
        return!(txtUserdId.getText().isEmpty()||pwd.length<4||repwd.length<4||txtQual.getText().isEmpty()||txtSpec.getText().isEmpty());
    }
      public boolean passwordMatch(String a, String b){
        return a.equals(b);
    }
