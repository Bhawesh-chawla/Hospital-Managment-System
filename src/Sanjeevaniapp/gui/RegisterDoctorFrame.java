       public void loadDoctorDetails(){
        try{
            doctor=DoctorDao.loadDocName();
            if(doctor.size()==0){
                JOptionPane.showMessageDialog(null,"No Unregistered Receptionist Present!");
                btnRegister.setEnabled(false);
                return;
            }
            btnRegister.setEnabled(true);
            Collection Keys=doctor.keySet();
            Iterator<String> it=Keys.iterator();
            jcbEid.removeAllItems();
            while(it.hasNext()){
                jcbEid.addItem(it.next());
            }
        }
        catch(SQLException sqle){
          JOptionPane.showMessageDialog(null,"DBError:"+sqle,"Exception",JOptionPane.ERROR_MESSAGE);
          sqle.printStackTrace();
        }
    }
